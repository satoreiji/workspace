#include <stdio.h>

int main(void){

    int a = 1;
    int b = 2;
    int c = 1;

    printf("a == b の値は %d です。\n", a == b);
    printf("a == c の値は %d です。\n", a == c);
    printf("a != b の値は %d です。\n", a != b);
    printf("a != c の値は %d です。\n", a != c);
    printf("a > b の値は %d です。\n", a > b);
    printf("a > c の値は %d です。\n", a > c);
    printf("a >= b の値は %d です。\n", a >= b);
    printf("a >= c の値は %d です。\n", a >= c);

    return 0;
}
